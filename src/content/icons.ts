/**
 * Icon slug. Can be either one of the bundled fallback icons (sparkles, shield, …)
 * or any slug exposed by the SVGL API (e.g. "svelte", "tailwindcss", "axiom-dark").
 */
export type IconId = string;

export type LocalIconId = (typeof icons)[number]['id'];

/**
 * Slugs that are intentionally *only* rendered locally (generic / utility
 * symbols that don't exist as brand SVGs on SVGL). For everything else we try
 * SVGL first and fall back to a local geometric icon on failure.
 */
const LOCAL_ONLY = new Set<string>([
  'sparkles',
  'shield',
  'star',
  'rocket',
  'terminal',
  'code',
  'lightning',
  'database',
  'newshields'
]);

export function isLocalOnly(id: string | null | undefined): boolean {
  if (!id) return false;
  return LOCAL_ONLY.has(id.toLowerCase());
}

/** Returns true if the slug has a hand-drawn local path bundled. */
export function isLocalIcon(id: string | null | undefined): boolean {
  if (!id) return false;
  return Object.prototype.hasOwnProperty.call(iconsById, id.toLowerCase());
}
